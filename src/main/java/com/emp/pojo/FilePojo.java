package com.emp.pojo;

public class FilePojo {
	private int fileTypeCode;
	private int saveLocation;

	public int getFileTypeCode() {
		return fileTypeCode;
	}

	public void setFileTypeCode(int fileTypeCode) {
		this.fileTypeCode = fileTypeCode;
	}

	public int getSaveLocation() {
		return saveLocation;
	}

	public void setSaveLocation(int saveLocation) {
		this.saveLocation = saveLocation;
	}
}
